// find the area of triangle.
 #include <stdio.h>
 int main()
  {
 	 int len,base, area;
 	 
	 printf(" \n Enter the length: ");
 	 scanf("%d",&len);
 	 
	printf(" \n Enter the base: ");
	scanf("%d",&base);
	 	  
	   area = 0.5 * len * base;
	   
	printf(" \n the area of the triangle is : %d ",area); 
	   
	   return 0;
 }
